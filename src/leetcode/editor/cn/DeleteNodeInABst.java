    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {

        /**
         * 这道题的重点在于只需交换节点的值，而不需要修改节点本身的关系
         */
        public TreeNode deleteNode(TreeNode root, int key) {
            if (root == null) {
                return null;
            }
            if (root.val < key) {
                root.right = deleteNode(root.right, key);
                return root;
            } else if (root.val > key) {
                root.left = deleteNode(root.left, key);
                return root;
            } else {
                if (root.right != null) {
                    root.val = searchLeft(root.right).val;
                    root.right = deleteNode(root.right, root.val);
                } else if (root.left != null) {
                    root.val = searchRight(root.left).val;
                    root.left = deleteNode(root.left, root.val);
                } else {
                    return null;
                }
                return root;
            }
        }

        private TreeNode searchRight(TreeNode node) {
            while (node.right != null) {
                node = node.right;
            }
            return node;
        }

        private TreeNode searchLeft(TreeNode node) {
            while (node.left != null) {
                node = node.left;
            }
            return node;
        }

    }
