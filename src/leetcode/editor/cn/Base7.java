    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public String convertToBase7(int num) {
            if (num == 0) return "0";
            StringBuilder builder = new StringBuilder();
            boolean neg = false;
            if (num < 0) {
                neg = true;
                num = -num;
            }
            while (num != 0) {
                builder.insert(0, num % 7);
                num = num / 7;
            }
            if (neg) {
                builder.insert(0, "-");
            }
            return builder.toString();
        }
    }
