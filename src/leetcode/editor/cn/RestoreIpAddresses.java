    public static void main(String[] args) {
        Solution solution = new RestoreIpAddresses().new Solution();
        print(solution.restoreIpAddresses("25525511135"));
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public List<String> restoreIpAddresses(String s) {
            List<String> list = new ArrayList<>();
            StringBuilder builder = new StringBuilder();
            recursive(list, builder, s, 0, 0);
            return list;
        }

        private void recursive(List<String> res, StringBuilder builder, String s, int begin, int n) {
            if (begin == s.length() && n > 3) {
                res.add(builder.substring(0, builder.length() - 1));
                return;
            }
            if (n < 3 || (n == 3 && begin >= s.length() - 3)) {
                for (int i = 1; i <= 3; i++) {
                    if (begin + i <= s.length()) {
                        String part = s.substring(begin, begin + i);
                        int partNum = Integer.parseInt(part);
                        if (!(part.startsWith("0") && part.length() > 1) && partNum >= 0 && partNum <= 255) {
                            builder.append(part);
                            builder.append(".");
                            recursive(res, builder, s, begin + i, n + 1);
                            builder.delete(builder.length() - i - 1, builder.length());
                        }
                    }
                }
            }
        }
    }
