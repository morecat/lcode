    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int countSegments(String s) {
            int res = 0;
            s = s + " ";
            for (int i=0;i<s.length()-1;i++) {
                if (s.charAt(i)!=' '&&s.charAt(i+1)==' '){
                    res++;
                }
            }
            return res;
        }
    }
