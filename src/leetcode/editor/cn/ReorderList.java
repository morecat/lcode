    public static void printList(ListNode node) {
        ListNode pr = node;
        while (pr != null) {
            System.out.print(pr.val + " ");
            pr = pr.next;
        }
    }

    public static class ListNode {
        int val;
        ListNode next;

        ListNode() {
        }

        ListNode(int val) {
            this.val = val;
        }

        ListNode(int val, ListNode next) {
            this.val = val;
            this.next = next;
        }
    }

