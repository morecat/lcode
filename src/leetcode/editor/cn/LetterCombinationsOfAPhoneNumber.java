    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {

        Map<Character, String> phoneMap = new HashMap<Character, String>() {{
            put('2', "abc");
            put('3', "def");
            put('4', "ghi");
            put('5', "jkl");
            put('6', "mno");
            put('7', "pqrs");
            put('8', "tuv");
            put('9', "wxyz");
        }};

        String digitsI = "";

        List<String> ret = new LinkedList<>();

        public List<String> letterCombinations(String digits) {
            if (digits.isEmpty()) {
                return Collections.emptyList();
            }
            digitsI = digits;
            dfs(0, "");
            return ret;
        }

        private void dfs(int height, String path) {
            if (height == digitsI.length()) {
                ret.add(path);
                return;
            }
            String digitLevel = phoneMap.get(digitsI.charAt(height));
            for (char c : digitLevel.toCharArray()) {
                dfs(height + 1, path + c);
            }
        }
    }
