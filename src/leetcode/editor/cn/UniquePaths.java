    class Solution {
        public int uniquePaths(int m, int n) {
            // c(a,b) = b!/a!(b-a)!
            // c(m-1,m+n-2) = (m+n-2)!/(m-1)!(m+n-m-1)!
            // 如果算法中涉及阶乘，一定要小心精度损失问题，这道题中如果不使用BigInteger，而是使用double或者long，都会出现一些case无法通过的情况
//            return factorial(m + n - 2).divide(factorial(m - 1)).divide(factorial(n - 1)).intValue();

            // 下面为动态规划解法
            int[][] dp = new int[m][n];
            for (int i = 0; i < m; ++i) {
                dp[i][0] = 1;
            }
            for (int j = 0; j < n; ++j) {
                dp[0][j] = 1;
            }
            for (int i = 1; i < m; i++) {
                for (int j = 1; j < n; j++) {
                    dp[i][j] = dp[i - 1][j] + dp[i][j - 1];
                }
            }
            return dp[m - 1][n - 1];
        }

        public java.math.BigInteger factorial(int n) {
            java.math.BigInteger result = java.math.BigInteger.ONE;
            for (int i = 1; i <= n; i++) {
                result = result.multiply(java.math.BigInteger.valueOf(i));
            }
            return result;
        }
    }
