    class Solution {

        List<List<Integer>> list;
        int[] travel;
        boolean res = true;

        public boolean canFinish(int numCourses, int[][] prerequisites) {
            travel = new int[numCourses];
            list = new ArrayList<>(numCourses);
            for (int i = 0; i < numCourses; i++) {
                list.add(new LinkedList<>());
            }
            for (int[] array : prerequisites) {
                List<Integer> sons = list.get(array[0]);
                sons.add(array[1]);
            }
            for (int i = 0; i < numCourses; i++) {
                if (!res) {
                    return false;
                }
                if (travel[i] == 0) {
                    dfs(i);
                }
            }
            return res;
        }

        private void dfs(int target) {
            travel[target] = 1;
            for (int item : list.get(target)) {
                if (travel[item] == 1) {
                    res = false;
                    return;
                } else if (travel[item] == 0) {
                    dfs(item);
                }
            }
            travel[target] = 2;
        }
    }
