    public static void main(String[] args) {
        Solution solution = new BinarySearch().new Solution();
        print(solution.search(new int[]{-1, 0, 3, 5, 9, 12}, 9));
        print(solution.search(new int[]{-1, 0, 3, 5, 9, 12}, 2));
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public int search(int[] nums, int target) {
            return searchRecursive(nums, target, 0, nums.length - 1);
        }

        private int searchRecursive(int[] nums, int target, int begin, int end) {
            int mid = (begin + end) / 2;
            if (begin >= end && target != nums[mid]) {
                return -1;
            }
            if (target == nums[mid]) {
                return mid;
            } else if (target < nums[mid]) {
                return searchRecursive(nums, target, begin, mid - 1);
            } else {
                return searchRecursive(nums, target, mid + 1, end);
            }
        }
    }
