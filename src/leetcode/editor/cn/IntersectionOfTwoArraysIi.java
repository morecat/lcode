class Solution {
    public int[] intersect(int[] nums1, int[] nums2) {
        int[] cache = new int[nums2.length];
        Arrays.sort(nums1);
        Arrays.sort(nums2);
        int i = 0;
        int j = 0;
        int k = 0;
        while (i < nums1.length && j < nums2.length) {
            if (nums1[i] < nums2[j]) {
                i++;
            } else if (nums1[i] > nums2[j]) {
                j++;
            } else {
                cache[k] = nums1[i];
                i++;
                j++;
                k++;
            }
        }
        int[] res = new int[k];
        System.arraycopy(cache, 0, res, 0, k);
        return res;
    }
}
