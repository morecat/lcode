/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode(int x) { val = x; }
 * }
 */

class Solution {

    /**
     * 官方解法：
     * 1. 两次遍历
     * 因为是二叉搜索树，所以每次遍历都是向下，最多到达叶子节点便一定会找到目标节点（如果不是二叉搜索树那么需要把左右子树全都检查一遍）
     * 因此先找到p节点，并把寻找过程的路径保存下来，再找到q节点，同样把寻找过程的路径保存下来，比较两个路径，取最后一个相同节点
     * 2. 一次遍历
     * 同时查找P,Q,遇到的第一个分岔点（大于p小于q）即所求。因为题目中说没有相同值的节点，因此取第一个分岔点没问题。
     * 下面采用一次遍历法求解
     */
    public TreeNode lowestCommonAncestor(TreeNode root, TreeNode p, TreeNode q) {
        TreeNode pointer = root;
        while (pointer != null) {
            if (pointer.val > p.val && pointer.val > q.val) {
                pointer = pointer.left;
            } else if (pointer.val < p.val && pointer.val < q.val) {
                pointer = pointer.right;
            } else {
                return pointer;
            }
        }
        return pointer;
    }
}
