/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public boolean isBalanced(TreeNode root) {
        return isBalancedPart(root);
    }

    private boolean isBalancedPart(TreeNode node) {
        if (node == null) {
            return true;
        }
        return (Math.abs(getDepth(node.left) - getDepth(node.right)) < 2)
                && isBalancedPart(node.left) && isBalancedPart(node.right);
    }

    private int getDepth(TreeNode node) {
        return getDepthPart(node);
    }

    private int getDepthPart(TreeNode node) {
        if (node == null) {
            return 0;
        }
        return Math.max(getDepthPart(node.left), getDepthPart(node.right)) + 1;
    }
}
