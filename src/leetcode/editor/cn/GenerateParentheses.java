    class Solution {

        public List<String> generateParenthesis(int n) {
            List<String> res = new ArrayList<>();
            StringBuilder builder = new StringBuilder();
            recursive(res, builder, 0, 0, n);
            return res;
        }

        private void recursive(List<String> res, StringBuilder builder, int open, int close, int max) {
            if (builder.length() == max * 2) {
                res.add(builder.toString());
                return;
            }
            if (open < max) {
                builder.append('(');
                recursive(res, builder, open + 1, close, max);
                builder.deleteCharAt(builder.length() - 1);
            }
            if (open > close) {
                builder.append(')');
                recursive(res, builder, open, close + 1, max);
                builder.deleteCharAt(builder.length() - 1);
            }
        }
    }
