    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {
        public TreeNode mergeTrees(TreeNode root1, TreeNode root2) {
            TreeNode res = mergeNode(root1, root2);
            dfs(root1, root2, res);
            return res;
        }

        private void dfs(TreeNode node1, TreeNode node2, TreeNode node3) {
            if (node3 == null) {
                return;
            }
            node3.left = mergeNode(node1 == null ? null : node1.left, node2 == null ? null : node2.left);
            node3.right = mergeNode(node1 == null ? null : node1.right, node2 == null ? null : node2.right);
            dfs(node1 == null ? null : node1.left, node2 == null ? null : node2.left, node3.left);
            dfs(node1 == null ? null : node1.right, node2 == null ? null : node2.right, node3.right);
        }

        private TreeNode mergeNode(TreeNode node1, TreeNode node2) {
            if (node1 == null && node2 == null) return null;
            int val1 = (node1 == null) ? 0 : node1.val;
            int val2 = (node2 == null) ? 0 : node2.val;
            return new TreeNode(val1 + val2);
        }
    }
