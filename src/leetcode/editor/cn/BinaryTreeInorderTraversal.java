/**
 * Definition for a binary tree node.
 * public class TreeNode {
 *     int val;
 *     TreeNode left;
 *     TreeNode right;
 *     TreeNode() {}
 *     TreeNode(int val) { this.val = val; }
 *     TreeNode(int val, TreeNode left, TreeNode right) {
 *         this.val = val;
 *         this.left = left;
 *         this.right = right;
 *     }
 * }
 */
class Solution {
    public List<Integer> inorderTraversal(TreeNode root) {
        List<Integer> result = new LinkedList<>();
        travelNode(result, root);
        return result;
    }

    /**
     * ä¸­åºéå† -> ä¸­æ ¹é¡»éå†ï¼Œå³è®¿é—®æ ¹ç»“ç‚¹çš„æ—¶æœºåœ¨ä¸­é—´ï¼ŒåŒºåˆ«äºå…ˆåºéå†ï¼ˆå‰åºéå†ï¼‰å’Œ ååºéå†
     */
    private void travelNode(List<Integer> result, TreeNode node) {
        if (node == null) {
            return;
        }
        travelNode(result, node.left);
        result.add(node.val);
        travelNode(result, node.right);
    }
}
