    class Solution {

        public int longestConsecutive(int[] nums) {
            // 下面为官方给的解法，官方只给了这么一种，这种主要是利用了hash（感觉官方的解法也没满足o(N)啊,民间有写法可以一次遍历完成，这里懒得看了）
            // 除了hash解法，还可以先排序后for循环（循环过程中有重复的子过程，此处采用动态规划优化），但是时间复杂度不满足o(N)
            // 但是并不知道哪里用到并查集了
            Set<Integer> num_set = new HashSet<Integer>();
            for (int num : nums) {
                num_set.add(num);
            }

            int longestStreak = 0;

            for (int num : num_set) {
                if (!num_set.contains(num - 1)) {
                    int currentNum = num;
                    int currentStreak = 1;

                    while (num_set.contains(currentNum + 1)) {
                        currentNum += 1;
                        currentStreak += 1;
                    }

                    longestStreak = Math.max(longestStreak, currentStreak);
                }
            }

            return longestStreak;
        }
    }
