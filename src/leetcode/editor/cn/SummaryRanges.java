class Solution {
    public List<String> summaryRanges(int[] nums) {
        int i = 0;
        int j = i;
        List<String> result = new ArrayList<>();
        if (nums.length == 0){
            return result;
        }
        while (j < nums.length - 1) {
            if (nums[j] + 1 != nums[j + 1]) {
                addRange(nums[i], nums[j], result);
                i = j + 1;
                j = i;
            } else {
                j++;
            }
        }
        addRange(nums[i], nums[j], result);
        return result;
    }

    private void addRange(int from, int to, List<String> list) {
        if (from == to) {
            list.add(from + "");
        } else {
            list.add(from + "->" + to);
        }
    }
}
