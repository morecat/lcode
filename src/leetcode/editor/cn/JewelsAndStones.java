    class Solution {
        public int numJewelsInStones(String jewels, String stones) {
            int num = 0;
            HashSet<Character> set = new HashSet<>();
            for (char c : jewels.toCharArray()) {
                set.add(c);
            }
            for (char c : stones.toCharArray()) {
                if (set.contains(c)) {
                    num++;
                }
            }
            return num;
        }
    }
