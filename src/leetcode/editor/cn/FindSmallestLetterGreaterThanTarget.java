    public static void main(String[] args) {
        Solution solution = new FindSmallestLetterGreaterThanTarget().new Solution();
        print(solution.nextGreatestLetter(new char[]{'c', 'f', 'j'}, 'a'));
        print(solution.nextGreatestLetter(new char[]{'c', 'f', 'j'}, 'c'));
        print(solution.nextGreatestLetter(new char[]{'c', 'f', 'j'}, 'd'));
        print(solution.nextGreatestLetter(new char[]{'c'}, 'd'));
        print(solution.nextGreatestLetter(new char[]{'c'}, 'a'));
    }

    //leetcode submit region begin(Prohibit modification and deletion)
    class Solution {
        public char nextGreatestLetter(char[] letters, char target) {
            int begin = 0;
            int end = letters.length;
            int mid = (begin + end) / 2;
            while (mid < end) {
                if (letters[mid] > target) {
                    end = mid;
                    mid = (begin + end) / 2;
                } else {
                    begin = mid + 1;
                    mid = (begin + end) / 2;
                }
            }
            if (mid >= letters.length) {
                return letters[0];
            }
            return letters[mid];
        }
    }
