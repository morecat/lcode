class Solution {
    public List<Integer> getRow(int rowIndex) {
        List<Integer> result = new ArrayList<>(rowIndex);
        for (int i = 0; i < rowIndex; i++) {
            result.add((int) c(rowIndex, i));
        }
        result.add(1);
        return result;
    }

    private long c(int n, int m) {
        return factorial(n).divide(factorial(m).multiply(factorial(n - m))).longValue();
    }

    private java.math.BigDecimal factorial(int x) {
        java.math.BigDecimal result = new java.math.BigDecimal(1);
        for (int i = 1; i <= x; i++) {
            result = result.multiply(new java.math.BigDecimal(i));
        }
        return result;
    }
}
