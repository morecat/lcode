class Solution {

    private final Map<Character, Integer> map = new HashMap<Character, Integer>() {{
        put('I', 1);
        put('V', 5);
        put('X', 10);
        put('L', 50);
        put('C', 100);
        put('D', 500);
        put('M', 1000);
    }};

    private final Map<String, Integer> mapExt = new HashMap<String, Integer>(){{
        put("IV", 4);
        put("IX", 9);
        put("XL", 40);
        put("XC", 90);
        put("CD", 400);
        put("CM", 900);
    }};

    public int romanToInt(String s) {
        if (s == null || s.isEmpty()) {
            return 0;
        }
        int sum = 0;
        for (Map.Entry<String, Integer> entry : mapExt.entrySet()) {
            while (s.contains(entry.getKey())) {
                // è¿™é‡Œæ›¿æ¢ä¸ºç©ºæ ¼è€Œä¸æ˜¯ç©ºå­—ç¬¦ï¼Œæ˜¯ä¸ºäº†é˜²æ­¢æ›¿æ¢ç‚¹å‰åç²˜è¿æˆæ–°çš„ç»„åˆè¯
                s = s.replaceFirst(entry.getKey(), " ");
                sum += entry.getValue();
            }
        }
        for (char c : s.toCharArray()) {
            if (c == ' '){
                continue;
            }
            sum += map.get(c);
        }
        return sum;
    }
}
