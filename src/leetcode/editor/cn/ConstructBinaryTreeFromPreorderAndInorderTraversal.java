    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {
        public TreeNode buildTree(int[] preorder, int[] inorder) {
            return buildSubTree(preorder, inorder, 0, 0, inorder.length);
        }

        private TreeNode buildSubTree(int[] preorder, int[] inorder, int parentIndex, int from, int to) {
            if (from >= to) {
                return null;
            }
            if (parentIndex >= preorder.length) {
                return null;
            }
            int parentVal = preorder[parentIndex];
            int pi = findIndex(inorder, from, to, parentVal);
            TreeNode leftNode = buildSubTree(preorder, inorder, parentIndex + 1, from, pi);
            TreeNode rightNode = buildSubTree(preorder, inorder, parentIndex + pi - from + 1, pi + 1, to);
            return new TreeNode(parentVal, leftNode, rightNode);
        }

        private int findIndex(int[] array, int from, int to, int val) {
            for (int i = from; i < to; i++) {
                if (array[i] == val) {
                    return i;
                }
            }
            return Integer.MIN_VALUE;
        }
    }
