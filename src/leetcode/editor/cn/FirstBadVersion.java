public class Solution extends VersionControl {
    public int firstBadVersion(int n) {
        long begin = 1;
        long end = n;
        long medium = (begin + end) / 2;
        while (begin <= end) {
            boolean curr = isBadVersion((int)medium);
            if (curr) {
                end = medium - 1;
                medium = (begin + end) / 2;
            } else {
                begin = medium + 1;
                medium = (begin + end) / 2;
            }
        }
        return (int)begin;
    }
}
