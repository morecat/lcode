    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {
        public List<Integer> rightSideView(TreeNode root) {
            List<Integer> res = new LinkedList<>();
            if (root == null) {
                return res;
            }
            Queue<TreeNode> queue = new LinkedList<>();
            Queue<TreeNode> row = new LinkedList<>();
            queue.offer(root);
            while (!queue.isEmpty()) {
                while (!queue.isEmpty()) {
                    row.offer(queue.poll());
                }
                TreeNode last = null;
                while (!row.isEmpty()) {
                    last = row.poll();
                    if (last.left != null) {
                        queue.offer(last.left);
                    }
                    if (last.right != null) {
                        queue.offer(last.right);
                    }
                }
                if (last != null) {
                    res.add(last.val);
                }
            }
            return res;
        }
    }
