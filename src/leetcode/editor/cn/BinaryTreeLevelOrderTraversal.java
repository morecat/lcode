    /**
     * Definition for a binary tree node.
     * public class TreeNode {
     * int val;
     * TreeNode left;
     * TreeNode right;
     * TreeNode() {}
     * TreeNode(int val) { this.val = val; }
     * TreeNode(int val, TreeNode left, TreeNode right) {
     * this.val = val;
     * this.left = left;
     * this.right = right;
     * }
     * }
     */
    class Solution {
        public List<List<Integer>> levelOrder(TreeNode root) {
            if (root == null) {
                return new ArrayList<>();
            }
            Queue<TreeNode> buf = new LinkedList<>();
            Queue<TreeNode> lineBuf = new LinkedList<>();
            List<List<Integer>> res = new ArrayList<>();
            buf.offer(root);
            while (!buf.isEmpty()) {
                List<Integer> lineList = new LinkedList<>();
                while (!buf.isEmpty()) {
                    TreeNode node = buf.poll();
                    lineBuf.offer(node);
                    lineList.add(node.val);
                }
                res.add(lineList);
                while (!lineBuf.isEmpty()) {
                    TreeNode node = lineBuf.poll();
                    if (node.left != null) {
                        buf.offer(node.left);

                    }
                    if (node.right != null) {
                        buf.offer(node.right);
                    }
                }
            }
            return res;
        }
    }
