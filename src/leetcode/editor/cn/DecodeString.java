    class Solution {

        int ptr = 0;

        public String decodeString(String s) {
            return decodePart(s);
        }

        private String decodePart(String s) {
            StringBuilder builder = new StringBuilder();
            while (ptr < s.length()) {
                if (Character.isDigit(s.charAt(ptr))) {
                    int reduce = decodeDigit(s, ptr);
                    ptr += Objects.toString(reduce).length() + 1;
                    String part = decodePart(s);
                    while (reduce-- > 0) {
                        builder.append(part);
                    }
                } else if (Character.isLetter(s.charAt(ptr))) {
                    builder.append(s.charAt(ptr));
                    ptr++;
                } else {
                    ptr++;
                    return builder.toString();
                }
            }
            return builder.toString();
        }

        private int decodeDigit(String s, int cur) {
            int ret = 0;
            for (int i = cur; i < s.length(); i++) {
                if (!Character.isDigit(s.charAt(i))) {
                    return ret;
                } else {
                    ret = ret * 10 + (s.charAt(i) - '0');
                }
            }
            return ret;
        }
    }
