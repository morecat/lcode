class Solution {
    public boolean isPerfectSquare(int num) {
        if (num == 1) {
            return true;
        }
        long begin = 1;
        long end = num / 2;
        long mid = (begin + end) / 2;
        while (begin <= end) {
            double div = (double) num / mid;
            double abs = (div > mid) ? (div - mid) : (mid - div);
            if (abs < 0.001) { //è¿™ä¸ªè¯¯å·®å€¼å¦‚æœé€‰æ‹©0.1ä¼šå‡ºç°é”™è¯¯, è¿™é‡Œçš„å€¼æˆ‘æ˜¯çŒœçš„
                return true;
            } else if (div > mid) {
                begin = mid + 1;
                mid = (begin + end) / 2;
            } else {
                end = mid - 1;
                mid = (begin + end) / 2;
            }
        }
        return false;
    }
}
